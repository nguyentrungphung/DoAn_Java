        public void nullText(){
        txt_name.setText("");
        txt_address.setText("");
        txt_email.setText("");
        txt_phone.setText("");
        spin_day.setValue(1);
        spin_month.setValue(1);
        Spin_year.setValue(1980);
        txt_acount.setText("");
    }
    public void showfindlist(){
        ArrayList<Employee> list;
        list = EmployeeController.findEmployees(txt_find.getText());
        for (Employee employee:list){
            model.addRow(employee.toArray());
        } 
        table_findlist.setModel(model);
        table_findlist.getColumnModel().getColumn(0).setPreferredWidth(170);
        table_findlist.getColumnModel().getColumn(1).setPreferredWidth(50);
    }
