    private void btn_acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_acceptActionPerformed
        try{
            String name = txt_name.getText();
            String gender = cbb_sex.getSelectedItem().toString();
            String birthday = spin_day.getValue().toString()+"/"+spin_month.getValue().toString()+"/"+Spin_year.getValue().toString();
            String phone = txt_phone.getText();
            String email = txt_email.getText();
            String address = txt_address.getText();
            String acount = txt_acount.getText();
            if(name.equals("") || email.equals("")|| address.equals("") || acount.equals("")){
                JOptionPane.showMessageDialog(null, "Thiếu thông tin!!","Cảnh báo",JOptionPane.WARNING_MESSAGE);
            }else{
                int checkphonenumber = Integer.parseInt(phone);
                EmployeeController.updateEmployee(acount,name, gender, birthday, phone, email, address);
                model.setRowCount(0);
                showfindlist();
                nullText();
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Sai format dữ liệu!!","Cảnh báo",JOptionPane.WARNING_MESSAGE);
        }
//        }catch(NullPointerException e1){
//            JOptionPane.showMessageDialog(null, "Không được để trống thông tin cần thiết!!","Cảnh báo",JOptionPane.WARNING_MESSAGE);
//        }
    }//GEN-LAST:event_btn_acceptActionPerformed

    private void table_findlistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_findlistMouseClicked
        if(evt.getClickCount() == 2){
            int row = table_findlist.getSelectedRow();
            txt_name.setText((String) model.getValueAt(row, 0));
            cbb_sex.setSelectedItem((String) model.getValueAt(row, 1));
            String birth[] = (model.getValueAt(row, 2).toString().split("/"));
            spin_day.setValue(Integer.parseInt(birth[0]));
            spin_month.setValue(Integer.parseInt(birth[1]));
            Spin_year.setValue(Integer.parseInt(birth[2]));
            txt_phone.setText((String) model.getValueAt(row, 3));
            txt_email.setText((String) model.getValueAt(row, 4));
            txt_address.setText((String) model.getValueAt(row, 5));
            txt_acount.setText((String) model.getValueAt(row, 6));
        }
    }//GEN-LAST:event_table_findlistMouseClicked

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if(txt_acount.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Không chọn được dữ liệu cần xóa!!","Lỗi",JOptionPane.ERROR_MESSAGE);
        }else{
            int n = JOptionPane.showConfirmDialog(null,"Bạn chắc chắn muốn xóa " + txt_acount.getText(),"Thông báo",JOptionPane.YES_NO_OPTION);
            if(n == JOptionPane.YES_OPTION){
                EmployeeController.deleteEmployee(txt_acount.getText());
                model.setRowCount(0);
                showfindlist();
                nullText();
            }else if(n == JOptionPane.NO_OPTION)
            {
                remove(n);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

