    private void cbb_tableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbb_tableActionPerformed
        //hiển thị table theo combobox bàn
        String table = cbb_table.getSelectedItem().toString();
        switch (table) {
            case "Bàn 2":
                modelorder1.fireTableDataChanged();
                table_order.setModel(modelorder1);
                break;
            case "Bàn 3":
                modelorder2.fireTableDataChanged();
                table_order.setModel(modelorder2);
                break;
            default:
                modelorder.fireTableDataChanged();
                table_order.setModel(modelorder);
                break;
        }
        
    }//GEN-LAST:event_cbb_tableActionPerformed

    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        //chon bàn - xóa sản phẩm
        String table = cbb_table.getSelectedItem().toString();
        switch (table) {
            case "Bàn 2":
                removeproduct(modelorder1);
                modelorder1.fireTableDataChanged();
                table_order.setModel(modelorder1);
                break;
            case "Bàn 3":
                removeproduct(modelorder2);
                modelorder2.fireTableDataChanged();
                table_order.setModel(modelorder2);
                break;
            default:
                removeproduct(modelorder);
                modelorder.fireTableDataChanged();
                table_order.setModel(modelorder);
                break;
        }
    }//GEN-LAST:event_btn_removeActionPerformed
     public void removeproduct(DefaultTableModel model){
        //xóa model tạo dòng đã chọn
        if(table_order.getSelectedRowCount() == 0)
        {
            JOptionPane.showMessageDialog(null, "Không xác định được sản phẩm cần hủy!!","Lỗi",JOptionPane.ERROR_MESSAGE);
        }else{
            model.removeRow(table_order.getSelectedRow());
        }
    }
    private void btn_orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_orderActionPerformed
        File file;
        FileOutputStream fos;
        String text = "";
        float giatien = 0;
        int countRow = table_order.getRowCount();
        for(int i = 0 ; i< countRow ; i++){
            text = text+"Tên sản phẩm: "+table_order.getValueAt(i, 1)+"\t Giá:"+table_order.getValueAt(i,3) +"\r\n";
            giatien = giatien + (int)(table_order.getValueAt(i, 3));
        }
        text =cbb_table.getSelectedItem()+"\r\n"+ text +"Tổng tiền: \t"+ giatien;
        try{
           file = new File("E:\\new.txt");
           fos = new FileOutputStream(file);
           if(!file.exists()){
               file.createNewFile();
           }
           byte[] contentInBytes = text.getBytes();
           fos.write(contentInBytes);
           fos.flush();
           fos.close();
           JOptionPane.showMessageDialog(null, "Thành công!!","thông báo",JOptionPane.INFORMATION_MESSAGE);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(OrderTable.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(OrderTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_orderActionPerformed
   
