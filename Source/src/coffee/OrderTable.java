    private void addorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addorderActionPerformed
        //thêm sản phẩm order
        String idproduct = txt_idproduct.getText();
        String productname =txt_productname.getText();
        int price = Integer.parseInt(txt_price.getText());
        String idtype = (String) cbb_idtype.getSelectedItem();        
        String table = cbb_table.getSelectedItem().toString();
        int quantities = (int)spin_quantity.getValue();
        Product product2 = new Product(idproduct,productname,idtype,price,quantities);
        int rowcount = table_order.getRowCount();
        switch (table) {
            case "Bàn 2":
                //kiểm tra sản phẩm đã thêm thì set lại số lượng
                if(rowcount>0){
                    for(int i = 0;i<rowcount;i++){
                        if(idproduct.equals(modelorder1.getValueAt(i, 0)))
                        {
                            modelorder1.removeRow(i);
                            break;
                        }
                    }
                    modelorder1.addRow(product2.toArray2());
                    table_order.setModel(modelorder1);
                    break;
                }
                modelorder1.addRow(product2.toArray2());
                table_order.setModel(modelorder1);
                break;
                
            case "Bàn 3":
                 if(rowcount>0){
                    for(int i = 0;i<rowcount;i++){
                        if(idproduct.equals(modelorder2.getValueAt(i, 0)))
                        {
                            modelorder2.removeRow(i);
                            break;
                        }
                    }
                    modelorder2.addRow(product2.toArray2());
                    table_order.setModel(modelorder2);
                    break;
                }
                modelorder2.addRow(product2.toArray2());
                table_order.setModel(modelorder2);
                break;
            case "Bàn 1":
                 if(rowcount>0){
                    for(int i = 0;i<rowcount;i++){
                        if(idproduct.equals(modelorder.getValueAt(i, 0)))
                        {
                            modelorder.removeRow(i);
                            break;
                        }
                    }
                    modelorder.addRow(product2.toArray2());
                    table_order.setModel(modelorder);
                    break;
                }
                modelorder.addRow(product2.toArray2());
                table_order.setModel(modelorder);
                break;
        }
    }//GEN-LAST:event_addorderActionPerformed

